# Merge df w/ Adm names ---------------------------------------------------
df3 = full_join(df2, rwAdm2, by = c("District" = "District"))

ggplot(df2, aes(x = District)) +
  geom_bar(stat = 'count') +
  facet_wrap(~IP)
  

